#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 && s2)
		while (n--)
			if (ft_tolower(*s1++) != ft_tolower(*s2++))
				return (0);
	return (1);
}
